    private void passwordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passwordFocusLost
        Archivos manejador = new Archivos();
        
        if(manejador.ValidarUsuario(login.getText(), password.getText())){
            
            Convertidor cvt = new Convertidor();
            Usuario user = new Usuario();
            user = cvt.StringUsuario(manejador.ObtenerUsuario(login.getText()));
            
            nivelAcceso.setValue(user.NivelAcceso);
            nombre.setText(user.Nombre);
            apellido.setText(user.Apellido);
            email.setText(user.Email);
        }
        
    }//GEN-LAST:event_passwordFocusLost

   
    private void Limpiar(){
        
        login.setText("");
        password.setText("");
        nivelAcceso.setValue(0);
        nombre.setText("");
        apellido.setText("");
        email.setText("");
    }
    
    private boolean esValido(){
        
        if(login.getText().isEmpty())
            return false;
        if(password.getText().isEmpty())
            return false;
        if((int) nivelAcceso.getValue() == 0)
            return false;
        if(nombre.getText().isEmpty())
            return false;
        if(apellido.getText().isEmpty())
            return false;
        return true;
    }
